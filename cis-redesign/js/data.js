// DYNAMIC MULTI-LANGUAGE DATABASE ROUTER
// ==========================================
// NOTE: The Arabic dataset is intentionally named CIS_DATA_AR (not CIS_DATA).
// A top-level `const CIS_DATA` would create a global lexical binding that
// shadows the `window.CIS_DATA` getter below, so every bare `CIS_DATA`
// reference in main.js would always read Arabic regardless of language.
// Keeping the only global `CIS_DATA` as this getter makes all consumers
// language-aware.
window.CIS_DATA_AR = CIS_DATA_AR;
Object.defineProperty(window, 'CIS_DATA', {
  get: function () {
    return (localStorage.getItem('cis_lang') || 'ar') === 'en' ? CIS_DATA_EN : window.CIS_DATA_AR;
  },
  configurable: true
});
